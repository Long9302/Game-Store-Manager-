def is_valid_customerID(cid):#checks if the customer id is written in the correct format
  if len(cid)!=4:#is 4 characters
      return False
  if cid.isalpha()==False:#is letters only
     return False
  return True


def is_valid_date(d):#checks the date aswell is in the correct format
  if len(d)!=10:
      return False
  if d[4]!="-" or d[7]!="-":#makes sure the dashes are in the right places
      return False

  y=d[0:4]#splicing to insert show year, month, day
  m=d[5:7]
  day=d[8:10]

  if not y.isdigit() or not m.isdigit() or not day.isdigit():#makes sure they are digits
      return False

  if int(m)<1 or int(m)>12:
      return False
  if int(day)<1 or int(day)>31:
      return False

  return True


def buildBooking_widget(file="Booking.txt"):#builds the actual widget

  out=widgets.Output()#all the widget boxes and waht they will show
  cid=widgets.Text(description="Customer:",placeholder="e.g. qxmd")
  d=widgets.Text(description="Date:",placeholder="YYYY-MM-DD")
  slot=widgets.Dropdown(options=["2pm","6pm"],description="Session:")
  g=widgets.Dropdown(options=[0,1,2,3],description="Guests:")
  button=widgets.Button(description="Book")

  def click(e):#when the book button is pressed it will run and clears previous output
        with out:
          clear_output()
          #collects all the inputs
          c=cid.value.strip().lower()
          date=d.value.strip()
          time=slot.value.strip().lower()
          guests=g.value

          if is_valid_customerID(c)==False:#make sure its valid
              print("Customer ID must be 4 letters")
              return

          if is_valid_date(date)==False:#makes sure date is valid
             print("Date must be YYYY-MM-DD")
             return

          if time!="2pm" and time!="6pm":#make sure only these 2 times are chosen but as im writing this i realise it wasn't needed
               print("Session must be 2pm or 6pm")
               return

          if str(guests).isdigit()==False:#makes the guests are a number
               print("Guests must be a number")
               return

          guests=int(guests)
          if guests<0 or guests>3: # and limits the amount of guest but also not needed
               print("Guests must be between 0 and 3")
               return

          count=0#stores people already booked
          f=open(file,"r")#checks capacity
          f.readline()
          for line in f:
              if "|" in line:
                   x=line.strip().split("|")
                   if len(x)==4:
                        if x[1]==date and x[2].lower()==time:
                             count=count+1+int(x[3])#adds the customer and guest
          f.close()

          if count+guests+1>50:#makes sure max 50 people
              print("That session is full")
              return

          f=open(file,"a")#saves the booking
          f.write(c+"|"+date+"|"+time+"|"+str(guests)+"\n")
          f.close()

          print("Booking confirmed")

  button.on_click(click)

  return widgets.VBox([#builds the layout of the outputs
     widgets.HTML("<h3>Booking System</h3>"),
     widgets.HBox([cid,d]),
     widgets.HBox([slot,g,button]),
     out
  ])



