def sepRow(line):#separates the columns in teh txt file with a '|' and removes extra spacing
  line = line.strip()
  if line == "":
    return []
  if "|" in line:
    stuff = line.split("|")
    newstuff = []
    for i in range(len(stuff)):
      bit = stuff[i].strip()
      newstuff.append(bit)
    return newstuff
  return line.split()

def loadBgames(filename):#returns a list of dictionaries from the board game txt for each game
  games= []
  try:
    file = open(filename, "r")
  except:
    return games

  firstline = file.readline()#skips header
  for line in file:
    line = line.strip()
    row = line.split("|")

    if len(row) < 6:
      continue
    g =row[0].strip()#uses 0 base indexing to allow me to access each column easier
    t =row[1].strip()
    gen =row[2].strip()
    minp =row[3].strip()
    maxp =row[4].strip()
    d =row[5].strip()

    if minp==maxp:
      play =minp
    else:
      play =minp + "-" + maxp
    oneGame ={}
    oneGame["id"] =g
    oneGame["name"] =t
    oneGame["genre"] =gen
    oneGame["players"] =play
    oneGame["date"] =d

    games.append(oneGame)
  file.close()
  return games
def returned_rental_info(filename):#looks at the rental txt to see the status
  allinfo ={}

  try:
    f= open(filename, "r")
  except:
    return allinfo

  f.readline()

  for line in f:
    bits =sepRow(line)

    if len(bits) < 2:
      continue

    gameid =bits[0].strip().lower()

    s = ""
    d = ""

    if len(bits) >=5:
      s = bits[4].strip().lower()
    if len(bits) >=4:
      d = bits[3].strip()

    result ={}
    result["status"] =s
    result["return_date"] =d

    allinfo[gameid] =result

  f.close()
  return allinfo


def gameAvailable(rInfo, gameID):#if game has been returned then it is available
  gid = gameID.strip().lower()

  if gid not in rInfo:
    return True

  info= rInfo[gid]
  st =info["status"]
  rd =info["return_date"]

  if st=="not returned":
    return False
  if st=="returned":
    return True
  if rd=="":
    return False

  return True


